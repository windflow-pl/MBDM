function A = calc_A(p)
    A(1,1) = 2.0*(p(1,1)*p(1,1) + p(2,1)*p(2,1) - 0.5);
    A(1,2) = 2.0*(p(2,1)*p(3,1) - p(1,1)*p(4,1));
    A(1,3) = 2.0*(p(2,1)*p(4,1) + p(1,1)*p(3,1));
    A(2,1) = 2.0*(p(2,1)*p(3,1) + p(1,1)*p(4,1));
    A(2,2) = 2.0*(p(1,1)*p(1,1) + p(3,1)*p(3,1) - 0.5);
    A(2,3) = 2.0*(p(3,1)*p(4,1) - p(1,1)*p(2,1));
    A(3,1) = 2.0*(p(2,1)*p(4,1) - p(1,1)*p(3,1));
    A(3,2) = 2.0*(p(3,1)*p(4,1) + p(1,1)*p(2,1));
    A(3,3) = 2.0*(p(1,1)*p(1,1) + p(4,1)*p(4,1) - 0.5);
end

